package com.gathering;

public enum ApiDocSpec {

	// ==================== 인증 관련 ====================
	LOGIN("로그인(/login)", """
		로그인이 성공하면 액세스 토큰 정보를 리턴하고 리프레시 토큰 쿠키가 발급된다.

		**실패 케이스**
		- 존재하지 않는 사용자로 로그인하면 404 에러가 발생한다.
		- 잘못된 비밀번호로 로그인하면 401 에러가 발생한다.
		- 유효하지 않은 이메일 형식으로 로그인 하면 400 에러가 발생한다.
		- 빈 비밀번호로 로그인하면 400 에러가 발생한다.
		"""),
	REFRESH("토큰 리프레시(/refresh)", """
		리프레시 토큰 쿠키를 사용하여 새로운 액세스 토큰을 발급받는다.

		**실패 케이스**
		- 리프레시 토큰이 없으면 401 에러가 발생한다.
		- 만료된 리프레시 토큰으로 갱신하면 401 에러가 발생한다.
		- 잘못된 형식의 리프레시 토큰으로 갱신하면 401 에러가 발생한다.
		- 취소된 리프레시 토큰으로 갱신하면 401 에러가 발생한다.
		"""),
	LOGOUT("로그아웃(/logout)", """
		로그아웃하면 리프레시 토큰이 삭제되고 무효화된다.
		리프레시 토큰이 없어도 로그아웃이 가능하다.
		"""),

	// ==================== 사용자 관련 ====================
	USER_JOIN("회원가입(/users/join)", """
		회원 정보가 DB에 저장되고 비밀번호는 암호화되어 저장된다.
		이메일은 중복될 수 없으며, 비밀번호는 AES로 암호화하여 전송해야 한다.
		"""),
	USER_GET("사용자 정보 조회(/users/{tsid})", """
		특정 사용자의 공개 정보(닉네임, 이름, 프로필 이미지)를 조회한다.
		민감한 정보(이메일, 전화번호, TSID)는 포함되지 않는다.

		**실패 케이스**
		- 삭제된 사용자를 조회하면 404 에러가 발생한다.
		- 정지된 사용자를 조회하면 404 에러가 발생한다.
		"""),
	USER_GET_ME("내 정보 조회(/users/me)", """
		로그인한 사용자의 상세 정보를 조회한다.
		이메일, 전화번호, 계정 상태, 비밀번호 설정 여부, 연동된 소셜 계정 목록이 포함된다.
		"""),
	USER_UPDATE_ME("내 정보 수정(/users/me)", """
		로그인한 사용자의 정보(닉네임, 이름, 전화번호)를 수정한다.
		변경하지 않을 필드는 null로 전송하면 된다.
		이메일은 변경할 수 없다.
		"""),
	USER_CHANGE_PASSWORD("비밀번호 변경(/users/me/password)", """
		현재 비밀번호를 확인한 후 새 비밀번호로 변경한다.
		비밀번호는 AES로 암호화하여 전송해야 한다.

		**실패 케이스**
		- 현재 비밀번호가 일치하지 않으면 400 에러가 발생한다.
		- 새 비밀번호가 정책(최소 8자, 숫자+특수문자 포함)을 위반하면 400 에러가 발생한다.
		"""),
	USER_UNLINK_OAUTH("소셜 연동 해제(/users/me/oauth/{provider})", """
		특정 OAuth 제공자와의 연동을 해제한다.
		비밀번호가 설정되어 있거나 다른 소셜 연동이 있어야 해제가 가능하다.

		**실패 케이스**
		- 비밀번호가 없고 마지막 소셜 연동을 해제하려 하면 400 에러가 발생한다.
		- 연동되지 않은 제공자를 해제하려 하면 404 에러가 발생한다.
		"""),
	USER_WITHDRAW("회원 탈퇴(/users/me)", """
		회원 탈퇴를 진행한다.
		비밀번호로 본인 확인 후 계정이 삭제 상태로 변경된다.
		연동된 모든 소셜 계정도 함께 해제된다.
		"""),

	// ==================== 모임 관련 ====================
	GATHERING_CREATE("모임 생성(/gatherings)", """
		모임이 생성되고 생성한 유저는 오너가 된다.
		모임 이름, 지역, 카테고리는 필수이며 설명과 대표 이미지는 선택사항이다.

		**실패 케이스**
		- 모임 이름을 누락하면 400 에러가 발생한다.
		- 카테고리를 누락하면 400 에러가 발생한다.
		- 존재하지 않는 지역으로 생성 요청하면 404 에러가 발생한다.
		"""),

	GATHERING_LIST("모임 목록 조회(/gatherings)", """
		모임 목록을 조회한다.
		카테고리와 지역으로 필터링할 수 있으며 커서 기반 무한 스크롤을 지원한다.

		**필터링**
		- categories: 다중 선택 가능 (쉼표 구분)
		- regionTsids: 다중 선택 가능

		**페이지네이션**
		- cursor가 없으면 첫 페이지 조회
		- hasNext=true일 때 nextCursor로 다음 페이지 조회
		"""),

	GATHERING_DETAIL("모임 상세 조회(/gatherings/{tsid})", """
		모임의 상세 정보를 조회한다.
		목록 정보에 더해 설명, 전체 참여자 리스트가 포함된다.
		참여자 리스트에는 닉네임, 프로필 이미지, role이 포함되어 OWNER를 구분할 수 있다.

		**실패 케이스**
		- 존재하지 않는 모임 조회 시 404 에러가 발생한다.
		"""),

	GATHERING_UPDATE("모임 정보 수정(/gatherings/{tsid})", """
		OWNER 또는 ADMIN이 모임 정보를 수정한다.
		PUT 방식으로 모든 필드를 전송해야 하며 필수값은 반드시 포함해야 한다.

		**수정 가능 필드**
		- name: 모임 이름 (필수, 25자 이하)
		- regionTsid: 지역 TSID (필수)
		- category: 카테고리 (필수)
		- description: 모임 설명 (선택, 1000자 이하)
		- mainImageUrl: 대표 이미지 URL (선택)

		**실패 케이스**
		- 모임 이름을 누락하면 400 에러가 발생한다.
		- 카테고리를 누락하면 400 에러가 발생한다.
		- 지역을 누락하면 400 에러가 발생한다.
		- 존재하지 않는 모임을 수정하면 404 에러가 발생한다.
		- 존재하지 않는 지역으로 수정하면 404 에러가 발생한다.
		- MEMBER 권한으로 수정하면 403 에러가 발생한다.
		- 모임에 참여하지 않은 사용자가 수정하면 403 에러가 발생한다.
		"""),

	GATHERING_DELETE("모임 삭제(/gatherings/{tsid})", """
		OWNER가 모임을 삭제한다.
		모임과 관련된 참여자 데이터가 함께 삭제된다.

		**권한**
		- OWNER만 삭제 가능
		- ADMIN은 삭제 권한 없음 (수정만 가능)

		**실패 케이스**
		- 존재하지 않는 모임을 삭제하면 404 에러가 발생한다.
		- ADMIN 권한으로 삭제하면 403 에러가 발생한다.
		- MEMBER 권한으로 삭제하면 403 에러가 발생한다.
		- 모임에 참여하지 않은 사용자가 삭제하면 403 에러가 발생한다.
		"""),

	GATHERING_CHANGE_PARTICIPANT_ROLE("참여자 역할 변경(/gatherings/{gatheringTsid}/participants/{targetUserTsid}/role)", """
		OWNER가 모임 참여자의 역할을 변경한다.
		변경 가능한 역할은 OWNER, ADMIN, MEMBER이다.
		OWNER로 역할을 변경하면 오너가 양도되고 기존 OWNER는 ADMIN으로 자동 변경된다.

		**권한**
		- OWNER만 역할 변경 가능
		- ADMIN, MEMBER는 역할 변경 불가

		**역할 변경 규칙**
		- MEMBER -> ADMIN: 관리자 권한 부여
		- ADMIN -> MEMBER: 관리자 권한 회수
		- MEMBER/ADMIN -> OWNER: 오너 양도 (기존 오너는 ADMIN이 됨)

		**실패 케이스**
		- ADMIN 권한으로 시도하면 403 에러가 발생한다.
		- MEMBER 권한으로 시도하면 403 에러가 발생한다.
		- 모임에 참여하지 않은 사용자가 시도하면 403 에러가 발생한다.
		- 존재하지 않는 모임의 역할 변경 시 404 에러가 발생한다.
		- 대상 유저가 모임 참여자가 아니면 404 에러가 발생한다.
		- 자신의 역할을 변경하려 하면 400 에러가 발생한다.
		"""),

	GATHERING_JOIN("모임 참여(/gatherings/{gatheringTsid}/participants)", """
		로그인한 사용자가 모임에 MEMBER로 참여한다.

		**권한**
		- 인증된 사용자 누구나 가능

		**참여 규칙**
		- 이미 참여중인 모임에는 다시 참여할 수 없음
		- 모임 정원이 가득 찬 경우 참여 불가
		- 참여 시 기본 역할은 MEMBER

		**실패 케이스**
		- 존재하지 않는 모임에 참여하면 404 에러가 발생한다.
		- 이미 참여중인 모임에 다시 참여하면 409 에러가 발생한다.
		- 정원이 가득 찬 모임에 참여하면 409 에러가 발생한다.
		"""),

	GATHERING_LEAVE("모임 퇴장(/gatherings/{gatheringTsid}/participants/me)", """
		로그인한 사용자가 모임에서 퇴장한다.

		**권한**
		- 해당 모임에 참여중인 사용자만 가능

		**퇴장 규칙**
		- MEMBER, ADMIN만 퇴장 가능
		- OWNER는 퇴장 불가 (오너 양도 또는 모임 삭제 필요)

		**실패 케이스**
		- 존재하지 않는 모임에서 퇴장하면 404 에러가 발생한다.
		- 참여하지 않은 모임에서 퇴장하면 404 에러가 발생한다.
		- OWNER가 퇴장하려 하면 400 에러가 발생한다.
		"""),

	// ==================== 지역 관련 ====================
	REGION_LIST("지역 목록 조회(/regions)", """
		전체 지역 목록을 계층 구조로 조회한다.
		depth-1 지역(서울특별시, 경기도)이 children 배열에 depth-2 지역(구/시)을 포함한다.

		**응답 구조**
		- 최상위 배열: depth-1 지역들 (서울특별시, 경기도)
		- 각 depth-1 지역의 children: 해당 지역의 구/시 목록
		- tsid: 모임 필터링에 사용
		- code: 행정구역 코드

		**모바일 최적화**
		- 단일 요청으로 전체 지역 데이터 로드 (56개 - 가벼움)
		- 클라이언트에서 캐싱 권장
		""");

	private final String summary;

	private final String description;

	ApiDocSpec(String summary, String description) {
		this.summary = summary;
		this.description = description;
	}

	public String getSummary() {
		return summary;
	}

	public String getDescription() {
		return description;
	}
}
